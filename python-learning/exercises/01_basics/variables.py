# Exercise 1: Variable assignment and printing
name = "Your Name"
age = 25
height = 5.9
is_student = True

print(f"Name: {name}")
print(f"Age: {age}")
print(f"Height: {height} feet")
print(f"Student: {is_student}")

# Exercise 2: Arithmetic operations
num1 = 10
num2 = 3

print(f"\nArithmetic with {num1} and {num2}:")
print(f"Addition: {num1 + num2}")
print(f"Subtraction: {num1 - num2}")
print(f"Multiplication: {num1 * num2}")
print(f"Division: {num1 / num2}")
print(f"Floor Division: {num1 // num2}")
print(f"Modulus: {num1 % num2}")
print(f"Exponent: {num1 ** num2}")

# Exercise 3: String operations
first_name = "Python"
last_name = "Learner"

full_name = first_name + " " + last_name
print(f"\nFull name: {full_name}")
print(f"Length: {len(full_name)}")
print(f"Uppercase: {full_name.upper()}")
print(f"Lowercase: {full_name.lower()}")

# Exercise 4: Type conversion
string_number = "100"
number = int(string_number)
print(f"\nConverted '{string_number}' to number: {number}")
print(f"Type: {type(number)}")

